package com.boda.xy;

public class DataTypeDemo {

	public static void main(String[] args) {
		byte a = 0b00101010;           // 二进制形式
		 int b = 0200;                   // 八进制形式
		long c = 0x1F;                  // 十六进制形式
		 
		System.out.println("a = "  + a);        //  输出：a = 42
		System.out.println("b = " + b);         //  输出：b = 128
		System.out.println("c = " + c);          // 输出：c = 31

		
		
	}

}
